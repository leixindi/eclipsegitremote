package com.leixindi.bean;

public class Person {
	/**
	 * 用户名
	 */
	private String userName;
	/**
	 * 年龄
	 */
	private int age;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
